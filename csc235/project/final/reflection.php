        This project was a little rough, I felt that the overall direction of the project wasn't really laid out in the instructions. This may be just that I am an outlier, trying to create an interactive entertaining experience.
        Specifically, I do not know if I am supposed to have "getJSONData.php" and "showJSONData.php" on my final submission. These files don't have anything to do with my project, and don't have a place in "dbdungeon" as a whole.
        I found it difficult sometimes to develop the required deliverables for each week's checkpoint, while developing details specific to my project, however this may also be an outlier.<br><br>

        The instructions were confusing in a lot of ways. I appreciate that the whole term project specs was available from the very first checkpoint. But the mentioning of things like Asana bogged down the readability of 
        the assignment with information that is not relevant to this term. A lot of the instructions, for both the final project and the individual assignments, felt like they were written for a different class. This made it
        difficult to interpret and follow the instructions from the perspective of this class. I can say with pretty high certainty that I didn't <strong>need</strong> to use Asana, and I didn't, but these were still present
        in the instructions and so I had chosen to not follow some bits of the instructions, to my own judgement.<br><br>

        I feel I attempted a more creative project that would be more fitting of a self-guided or capstone project, rather than a project specifically guided on Server Side Web Development.<br><br>

        I still enjoyed this project, I think it turned out less interesting than I imagined it could be, as a game. But I learned a good deal about databases in order to properly do this.<br><br>

        <strong>I have not</strong> done playtesting all the way through, it is possible that the actual puzzle has some undiscovered obstacle that makes the whole thing unsolvable. But it exists as a thing you can play.
